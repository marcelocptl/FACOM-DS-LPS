public class Completo {
	
		private String posicaoCliente;
	
		private String adverso;
	
		private String posicaoAdverso;
	
		private String advogadoAdverso;
	
		private String localidadeFaseAtual;
	
		private String forumFaseAtual;
	
		private String vara;
	
	
	
		public final String getPosicaoCliente() {
			return this.posicaoCliente;
		}
	
		public final String getAdverso() {
			return this.adverso;
		}
	
		public final String getPosicaoAdverso() {
			return this.posicaoAdverso;
		}
	
		public final String getAdvogadoAdverso() {
			return this.advogadoAdverso;
		}
	
		public final String getLocalidadeFaseAtual() {
			return this.localidadeFaseAtual;
		}
	
		public final String getForumFaseAtual() {
			return this.forumFaseAtual;
		}
	
		public final String getVara() {
			return this.vara;
		}
	
	
	
		public final void setPosicaoCliente(final String somePosicaoCliente) {
			this.posicaoCliente = somePosicaoCliente;
		}
	
		public final void setAdverso(final String someAdverso) {
			this.adverso = someAdverso;
		}
	
		public final void setPosicaoAdverso(final String somePosicaoAdverso) {
			this.posicaoAdverso = somePosicaoAdverso;
		}
	
		public final void setAdvogadoAdverso(final String someAdvogadoAdverso) {
			this.advogadoAdverso = someAdvogadoAdverso;
		}
	
		public final void setLocalidadeFaseAtual(final String someLocalidadeFaseAtual) {
			this.localidadeFaseAtual = someLocalidadeFaseAtual;
		}
	
		public final void setForumFaseAtual(final String someForumFaseAtual) {
			this.forumFaseAtual = someForumFaseAtual;
		}
	
		public final void setVara(final String someVara) {
			this.vara = someVara;
		}
	
}